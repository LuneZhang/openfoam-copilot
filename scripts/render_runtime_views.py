def render_view(kind: str, state: Dict[str, Any]) -> str:
    if kind == "agent-entry":
        return render_agent_entry(state)
    if kind == "troubleshooting-entry":
        return render_troubleshooting_entry(state)
    if kind == "retrieval-order":
        return render_retrieval_order(state)
    if kind == "skill-bridge":
        return render_skill_bridge(state)
    raise RenderError(f"unsupported render kind '{kind}'")


def target_path_for_kind(kind: str) -> Path:
    return GENERATED_ROOT / KIND_TO_FILENAME[kind]


def selected_kinds(kind: str) -> List[str]:
    return [kind] if kind else list(KIND_ORDER)


def write_views(state: Dict[str, Any], kind: str) -> int:
    GENERATED_ROOT.mkdir(parents=True, exist_ok=True)
    written = 0
    for target_kind in selected_kinds(kind):
        output_path = target_path_for_kind(target_kind)
        output_path.write_text(render_view(target_kind, state), encoding="utf-8")
        print(f"WROTE [{target_kind}] {repo_relative(output_path)}")
        written += 1
    print(f"Summary: wrote {written} generated runtime view(s).")
    return 0


def diff_preview(expected: str, actual: str) -> List[str]:
    diff_lines = list(
        difflib.unified_diff(
            expected.splitlines(),
            actual.splitlines(),
            fromfile="expected",
            tofile="actual",
            lineterm="",
        )
    )
    if len(diff_lines) > 40:
        diff_lines = diff_lines[:40] + ["... diff truncated ..."]
    return diff_lines


def read_text(path: Path) -> str:
    try:
        return path.read_text(encoding="utf-8")
    except FileNotFoundError as exc:
        raise RenderError(f"missing comparison file: {path}") from exc


def compare_one(kind: str, expected: str, path: Path) -> bool:
    actual = read_text(path)
    if actual == expected:
        try:
            location = repo_relative(path)
        except ValueError:
            location = str(path)
        print(f"PASS [{kind}] {location}")
        return True

    try:
        location = repo_relative(path)
    except ValueError:
        location = str(path)
    print(f"FAIL [{kind}] {location}")
    for line in diff_preview(expected, actual):
        print(f"  {line}")
    return False


def check_views(state: Dict[str, Any], kind: str, compare_file: str) -> int:
    failures = 0
    checked = 0

    if compare_file:
        if not kind:
            raise RenderError("--compare-file requires --kind")
        checked += 1
        if not compare_one(
            kind, render_view(kind, state), Path(compare_file).expanduser()
        ):
            failures += 1
    else:
        for target_kind in selected_kinds(kind):
            checked += 1
            if not compare_one(
                target_kind,
                render_view(target_kind, state),
                target_path_for_kind(target_kind),
            ):
                failures += 1

    print(f"Summary: checked {checked} generated runtime view(s), {failures} drifted.")
    return 1 if failures else 0


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Render and drift-check runtime-generated markdown views."
    )
    action = parser.add_mutually_exclusive_group(required=True)
    action.add_argument(
        "--write",
        action="store_true",
        help="Write rendered views under runtime/generated/.",
    )
    action.add_argument(
        "--check",
        action="store_true",
        help="Check generated views for drift without writing files.",
    )
    parser.add_argument(
        "--kind",
        choices=sorted(KIND_TO_FILENAME.keys()),
        help="Render or check only one generated view kind.",
    )
    parser.add_argument(
        "--compare-file",
        help="Compare rendered output for --kind against an arbitrary file path.",
    )
    args = parser.parse_args()
    if args.write and args.compare_file:
        parser.error("--compare-file can only be used with --check")
    if args.compare_file and not args.kind:
        parser.error("--compare-file requires --kind")
    return args


def main() -> int:
    args = parse_args()
    state = build_runtime_state()
    if args.write:
        return write_views(state, args.kind)
    return check_views(state, args.kind, args.compare_file)


if __name__ == "__main__":
    try:
        sys.exit(main())
    except RenderError as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        sys.exit(1)
