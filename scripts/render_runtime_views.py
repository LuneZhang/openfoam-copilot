#!/usr/bin/env python3

import argparse
import difflib
import json
import sys
from pathlib import Path
from typing import Any, Dict, Iterable, List, Sequence, Tuple


REPO_ROOT = Path(__file__).resolve().parent.parent
RUNTIME_ROOT = REPO_ROOT / "runtime"
GENERATED_ROOT = RUNTIME_ROOT / "generated"
CATALOG_ROOT = RUNTIME_ROOT / "catalog"

KIND_TO_FILENAME = {
    "agent-entry": "agent-entry.md",
    "troubleshooting-entry": "troubleshooting-entry.md",
    "retrieval-order": "retrieval-order.md",
    "skill-bridge": "skill-bridge.md",
}

KIND_ORDER = tuple(KIND_TO_FILENAME)
CANONICAL_INPUTS = (
    RUNTIME_ROOT / "contract.json",
    RUNTIME_ROOT / "surface.json",
    CATALOG_ROOT / "scenarios.json",
    CATALOG_ROOT / "playbooks.json",
    CATALOG_ROOT / "nodes.json",
    CATALOG_ROOT / "prompts.json",
    CATALOG_ROOT / "sources.json",
)
GENERATED_BANNER = (
    "<!-- Generated by scripts/render_runtime_views.py from "
    "runtime/contract.json, runtime/surface.json, and runtime/catalog/*.json. -->"
)


class RenderError(Exception):
    pass


def normalize_path(value: str) -> str:
    return value.replace("\\", "/")


def repo_relative(path: Path) -> str:
    return normalize_path(str(path.relative_to(REPO_ROOT)))


def load_json(path: Path) -> Any:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except FileNotFoundError as exc:
        raise RenderError(f"missing runtime input: {repo_relative(path)}") from exc
    except json.JSONDecodeError as exc:
        raise RenderError(
            f"invalid JSON in {repo_relative(path)} at line {exc.lineno}, column {exc.colno}: {exc.msg}"
        ) from exc


def require_object(value: Any, label: str) -> Dict[str, Any]:
    if not isinstance(value, dict):
        raise RenderError(f"{label} must be a JSON object")
    return value


def require_list(value: Any, label: str) -> List[Any]:
    if not isinstance(value, list):
        raise RenderError(f"{label} must be a JSON array")
    return value


def require_string(value: Any, label: str) -> str:
    if not isinstance(value, str) or not value.strip():
        raise RenderError(f"{label} must be a non-empty string")
    return value


def require_string_list(value: Any, label: str) -> List[str]:
    items = require_list(value, label)
    result: List[str] = []
    for index, item in enumerate(items):
        result.append(require_string(item, f"{label}[{index}]").strip())
    return result


def load_catalog(name: str) -> Dict[str, Any]:
    payload = require_object(
        load_json(CATALOG_ROOT / f"{name}.json"), f"catalog:{name}"
    )
    items = require_list(payload.get("items"), f"catalog:{name}.items")
    for index, item in enumerate(items):
        entry = require_object(item, f"catalog:{name}.items[{index}]")
        require_string(entry.get("id"), f"catalog:{name}.items[{index}].id")
        require_string(entry.get("path"), f"catalog:{name}.items[{index}].path")
        require_string(entry.get("status"), f"catalog:{name}.items[{index}].status")
        require_string_list(
            entry.get("aliases", []), f"catalog:{name}.items[{index}].aliases"
        )
    return payload


def active_items(catalog: Dict[str, Any]) -> List[Dict[str, Any]]:
    items = require_list(catalog.get("items"), "catalog.items")
    active: List[Dict[str, Any]] = []
    for item in items:
        entry = require_object(item, "catalog.items[]")
        if entry.get("status") == "active":
            active.append(entry)
    return active


def item_by_id(catalog: Dict[str, Any], item_id: str) -> Dict[str, Any]:
    for item in active_items(catalog):
        if item.get("id") == item_id:
            return item
    raise RenderError(f"missing active catalog item '{item_id}'")


def format_alias_suffix(item: Dict[str, Any]) -> str:
    aliases = [
        alias for alias in item.get("aliases", []) if isinstance(alias, str) and alias
    ]
    if not aliases:
        return ""
    quoted = ", ".join(f"`{alias}`" for alias in aliases)
    return f" (aliases: {quoted})"


def format_catalog_line(item: Dict[str, Any]) -> str:
    return f"- `{item['id']}` -> `{item['path']}`{format_alias_suffix(item)}"


def format_path_lines(paths: Iterable[str]) -> List[str]:
    return [f"- `{path}`" for path in paths]


def format_indented_path_lines(paths: Iterable[str]) -> List[str]:
    return [f"   - `{path}`" for path in paths]


def format_indented_plain_lines(items: Iterable[str]) -> List[str]:
    return [f"   - {item}" for item in items]


def render_markdown_list(items: Sequence[str]) -> List[str]:
    return [f"{index}. {item}" for index, item in enumerate(items, start=1)]


def first_present(paths: Sequence[str], target: str) -> bool:
    return any(path == target for path in paths)


def build_runtime_state() -> Dict[str, Any]:
    contract = require_object(load_json(RUNTIME_ROOT / "contract.json"), "contract")
    surface = require_object(load_json(RUNTIME_ROOT / "surface.json"), "surface")
    scenarios = load_catalog("scenarios")
    playbooks = load_catalog("playbooks")
    nodes = load_catalog("nodes")
    prompts = load_catalog("prompts")
    sources = load_catalog("sources")

    contract_entrypoints = require_string_list(
        contract.get("runtime_entrypoints"), "contract.runtime_entrypoints"
    )
    optional_helpers = require_string_list(
        contract.get("optional_helpers"), "contract.optional_helpers"
    )
    fallback_behavior = require_object(
        contract.get("fallback_behavior"), "contract.fallback_behavior"
    )
    manual_inputs = require_string_list(
        fallback_behavior.get("minimum_manual_inputs"),
        "contract.fallback_behavior.minimum_manual_inputs",
    )
    helper_fallback = require_string_list(
        fallback_behavior.get("when_optional_helpers_are_unavailable"),
        "contract.fallback_behavior.when_optional_helpers_are_unavailable",
    )
    surface_policy = require_object(
        contract.get("runtime_surface_policy"), "contract.runtime_surface_policy"
    )
    default_runtime_dependencies = require_string_list(
        surface_policy.get("default_runtime_dependencies"),
        "contract.runtime_surface_policy.default_runtime_dependencies",
    )
    default_retrieval_policy = require_object(
        surface.get("default_retrieval_policy"), "surface.default_retrieval_policy"
    )
    entry_surfaces = require_string_list(
        default_retrieval_policy.get("entry_surfaces"),
        "surface.default_retrieval_policy.entry_surfaces",
    )
    include_runtime_support_when = require_string_list(
        default_retrieval_policy.get("include_runtime_support_when"),
        "surface.default_retrieval_policy.include_runtime_support_when",
    )
    exclude_by_default = require_string_list(
        default_retrieval_policy.get("exclude_by_default"),
        "surface.default_retrieval_policy.exclude_by_default",
    )

    active_scenarios = active_items(scenarios)
    active_playbooks = active_items(playbooks)
    active_nodes = active_items(nodes)
    active_prompts = active_items(prompts)
    active_sources = active_items(sources)

    routing_playbooks = [
        item
        for item in active_playbooks
        if "/debug-routing/" in str(item.get("path", ""))
    ]
    operational_playbooks = [
        item
        for item in active_playbooks
        if "/debug-routing/" not in str(item.get("path", ""))
    ]

    prompt_map = {item["id"]: item for item in active_prompts}
    generated_paths = {
        kind: repo_relative(GENERATED_ROOT / filename)
        for kind, filename in KIND_TO_FILENAME.items()
    }

    return {
        "contract": contract,
        "surface": surface,
        "contract_entrypoints": contract_entrypoints,
        "optional_helpers": optional_helpers,
        "manual_inputs": manual_inputs,
        "helper_fallback": helper_fallback,
        "default_runtime_dependencies": default_runtime_dependencies,
        "entry_surfaces": entry_surfaces,
        "include_runtime_support_when": include_runtime_support_when,
        "exclude_by_default": exclude_by_default,
        "scenarios": active_scenarios,
        "playbooks": active_playbooks,
        "routing_playbooks": routing_playbooks,
        "operational_playbooks": operational_playbooks,
        "nodes": active_nodes,
        "prompts": active_prompts,
        "prompt_map": prompt_map,
        "sources": active_sources,
        "sources_catalog": sources,
        "generated_paths": generated_paths,
        "parallel_triage_available": first_present(
            require_string_list(
                surface.get("runtime_public"), "surface.runtime_public"
            ),
            "PARALLEL_TRIAGE_DECISION_TREE.md",
        ),
        "setup_playbook": item_by_id(playbooks, "first-pass-case-setup-checklist"),
        "setup_prompt": prompt_map.get("setup-assistant"),
        "troubleshooting_prompt": prompt_map.get("troubleshooting-assistant"),
        "review_prompt": prompt_map.get("case-review"),
        "source_prompt": prompt_map.get("source-triage-assistant"),
    }


